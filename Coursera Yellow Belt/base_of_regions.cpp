using namespace std;

enum class Lang {
  DE, FR, IT
};

struct Region {
  string std_name;
  string parent_std_name;
  map<Lang, string> names;
  int64_t population;
};

bool operator<(const Region& one, const Region& two){
	return tie(one.std_name, one.parent_std_name, one.names, one.population) <
			tie(two.std_name, two.parent_std_name, two.names, two.population);
}


int FindMaxRepetitionCount(const vector<Region>& regions){
	map<Region, int> repites;
	for (auto& i : regions){
		repites[i] += 1;
	}
	int num = 0;
	for (auto& [key, value] : repites){
		if (value > num){
			num = value;
		}
	}
	return num;
}

